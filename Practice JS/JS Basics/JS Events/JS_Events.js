/*
JavaScript Events

Javascript has events to provide a dynamic interface to a webpage. 
These events are hooked to elements in the Document Object Model(DOM). 
These events by default use bubbling propagation i.e, upwards in the DOM from 
children to parent. We can bind events either as inline or in an external script. 
*/

/*
Some JavaScript Events

1. onclick event: This is a mouse event and provokes any logic defined if the user clicks on the 
element it is bound to.

2. onkeyup event: This event is a keyboard event and executes instructions whenever a key is 
released after pressing. 

3. onmouseover event: This event corresponds to hovering the mouse pointer over the element and 
its children, to which it is bound to. 

4. onmouseout event: Whenever the mouse cursor leaves the element which handles a mouseout event, 
a function associated with it is executed. 

5. onchange event: This event detects the change in value of any element listing to this event. 

6. onload event: When an element is loaded completely, this event is evoked. 

7. onfocus event: An element listing to this event executes instructions whenever it receives focus.

8. onblur event: This event is evoked when an element loses focus. 

JavaScript onmouseup event listens to left and middle mouse clicks, but onmousedown event 
listens to left, middle, and right mouse clicks whereas onclick only handles left clicks.
*/
